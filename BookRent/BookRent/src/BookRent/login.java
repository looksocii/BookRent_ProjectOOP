    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        

        //edited---------------------------------------------------------------------------------
        //เช็ค username กับ passwords ในตารางของข้อมูล Customer ใน DataBase
        String txt1 = jTextField1.getText();
        String txt2 = jPasswordField1.getText();
        
        int status = 0; //ตัวแปรที่ใช้เช็คว่าใครเป็นคน login เข้ามา
        // status = 0 คือ ไม่เจอข้อมูล
        // status = 1 คือ Personnel
        // status = 2 คือ Customer
        // status = 3 คือ Admin
        
        try {
            //เช็ค Personnel
            staff.beforeFirst();
            while(staff.next()){
                if(staff.getString(2).equals(txt1) && staff.getString(3).equals(txt2)){
                    ShareMethod.setFirstName(staff.getString(4));
                    ShareMethod.setLastName(staff.getString(5));
                    ShareMethod.setUsername(staff.getString(2));
                    status = 1;
                }
            }
            staff.beforeFirst();
            
            //เช็ค Customer
            user.beforeFirst();
            while(user.next()){
                if(user.getString(2).equals(txt1) && user.getString(3).equals(txt2)){
                    ShareMethod.setFirstName(user.getString(4));
                    ShareMethod.setLastName(user.getString(5));
                    ShareMethod.setUsername(user.getString(2));
                    status = 2;
                }
            }
            user.beforeFirst();
            
            //เช็ค Admin และ Set
            String admin = "admin";
            String pass = "0000";
            if(txt1.equals(admin) && txt2.equals(pass)){
                ShareMethod.setFirstName("Admin");
                ShareMethod.setLastName("");
                ShareMethod.setUsername("admin");
                status = 3;
            }
            
            //เปิดหน้าต่างใหม่ตามผู้ใช้ที่ login เข้ามา
            if(status == 0){
                JOptionPane.showMessageDialog(null, "Username or Passwords Invalid.");
            }else if(status == 1){
                new staff().setVisible(true);
                this.dispose();
            }else if(status == 2){
                new user().setVisible(true);
                this.dispose();
            }else if(status == 3){
                new admin().setVisible(true);
                this.dispose();
            }
        } catch (Exception e) {
        }
        //edited---------------------------------------------------------------------------------
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

